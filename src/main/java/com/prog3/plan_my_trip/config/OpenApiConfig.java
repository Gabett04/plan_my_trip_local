package com.prog3.plan_my_trip.config;

import io.swagger.v3.oas.models.Components;
import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Contact;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.info.License;
import io.swagger.v3.oas.models.security.SecurityScheme;
import io.swagger.v3.oas.models.security.SecurityScheme.Type;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class OpenApiConfig {
    @Bean
    public OpenAPI customOpenAPI() {
        return (new OpenAPI()).components((new Components()).addSecuritySchemes("bearerAuth", (new SecurityScheme()).type(Type.HTTP).scheme("bearer").bearerFormat("JWT"))).info((new Info()).title("PlanMyTrip API").version("1.0.0").description("API REST para la plataforma de planificación de viajes PlanMyTrip").contact((new Contact()).name("Universidad de Córdoba").email("contacto@planmytrip.com")).license((new License()).name("Apache 2.0").url("https://www.apache.org/licenses/LICENSE-2.0")));
    }
}
